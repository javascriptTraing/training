// we need to find the grade of Mark
// if 0 - 40 grade D
// if 40 - 60 grade C
// if 60 - 80 grade B
// if 80 - 100 grade A
var score = 50
if(score < 40) {
    console.log("Grade D")
}
else if (score <60) {
    console.log("Grade C")
}
else if (score <80) {
    console.log("Grade B")
}
else if (score <100) {
    console.log("Grade A")
}
