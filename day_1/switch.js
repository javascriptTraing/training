//switch case is used to map condition to value
var country_name = 'US'
switch(country_name) {
    case 'IE':
        console.log('Ireland')
        break
    case 'UK':
        console.log('United Kindom')
        break
    case 'US':
        console.log('United States')
        break
    case 'CN':
        console.log('China')
        break
    default:
        console.log('unknown Country code')
}
